	<h2 class="text-3xl font-bold">
		3D Platformer
	</h2>
	
	<div class="space-y-3">
		<p class="text-lg">
			A 3D platformer inspired by Super Mario 3D World, developed in Godot as a showcase of game feel for a university taster session.
		</p>
		
		<video src="/3d-platformer/demo.mp4" autoplay muted loop playsinline controls width="600" class="rounded-xl shadow-lg">
			<track kind="captions" />
		</video>
		
		<p class="text-lg">
			The students could tweak various parameters to get a sense for how they affect the feel of the character, and explore a simple platformer environment I build out of transformed rock models.
		</p>
		<div class="space-y-0">
			<p class="text-lg">
				Movement parameters:
			</p>
			<ul class="list-disc pl-8">
				<li>Acceleration</li>
				<li>Jump Height</li>
				<li>Time to reach apex</li>
				<li>Time to descend from apex</li>
			</ul>
		</div>
		<div class="space-y-0">
			<p class="text-lg">
				Aesthetic Parameters
			</p>
			<ul class="list-disc pl-8">
				<li>Presence of trailing cloud particles</li>
				<li>Speed threshold to spawn trailing particles</li>
				<li>Presence of sound including running, jump, and double jump sound effects</li>
			</ul>
		</div>
		<div class="space-y-0">
			<p class="text-lg">
				Asset Credits
